    private void btn_signupActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_signupActionPerformed
        // TODO add your handling code here:
        getData();
        if(pcl.countRowbyUser(username) == 0) {
            pcl.insert(username, password, firstname, lastname, gender, phonenumber, email);
            lb_note.setText("Sign up succesfully");
            new JF_Login().setVisible(true);
            this.dispose();
        } else {
            lb_note.setText("Sign up fail");
        }
        
    }//GEN-LAST:event_btn_signupActionPerformed

    public void getData() {
        username = txt_username.getText();
        password = txt_password.getText();
        email = txt_email.getText();
        phonenumber = txt_phonenumber.getText();
        firstname = txt_firstname.getText();
        lastname = txt_lastname.getText();
        if(rbtn_male.isSelected()) {
            gender = "male";
        }
        if(rbtn_female.isSelected()) {
            gender = "female";
        }
    }
