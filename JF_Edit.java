    public void get_dataMenu(int id) {
        rs = pcl.getById(id);
        try {
            while(rs.next()){
                txt_username.setText(rs.getString("username"));
                txt_password.setText(rs.getString("password"));  
                txt_firstname.setText(rs.getString("firstname"));
                txt_lastname.setText(rs.getString("lastname"));
                txt_phonenumber.setText(rs.getString("phonenumber"));
                txt_email.setText(rs.getString("email"));
                String gender = rs.getString("gender");
                if(gender.equalsIgnoreCase("male")) {
                    rbtn_male.setSelected(true);
                } else {
                    rbtn_female.setSelected(true);
                }
            }
        } catch (Exception ex) {
            ex.printStackTrace();
        }
    }
    
    public void get_dataEdit() {
        username = txt_username.getText();
        password = txt_password.getText();
        email = txt_email.getText();
        phonenumber = txt_phonenumber.getText();
        firstname = txt_firstname.getText();
        lastname = txt_lastname.getText();
        if(rbtn_male.isSelected()) {
            gender = "male";
        }
        if(rbtn_female.isSelected()) {
            gender = "female";
        }
    }

