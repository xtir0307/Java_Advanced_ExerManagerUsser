    public void showData() {
        try {
            ResultSet rs = pcl.getAll();
            while(rs.next()) {
                Object[] rows = {
                    rs.getString("username"), rs.getString("password") , rs.getString("firstname"), rs.getString("lastname"), rs.getString("gender"), rs.getString("phonenumber"), rs.getString("email")
                };
                tableModel.addRow(rows);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }
